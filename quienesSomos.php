                <h2>¿Dónde nace la idea?</h2>
                <p>FISPE es un proyecto desarrollado desde la academia, con la finalidad de entregar una herramienta en salud para toda la comunidad.</p>
                <p>Desarrollada por estudiantes comprometidos con la mejora del bienestar comunitario, es una innovadora herramienta diseñada para apoyar a las poblaciones más vulnerables. Esta ficha permite un monitoreo continuo y detallado de las condiciones de salud de los individuos, facilitando la identificación temprana de posibles problemas y la implementación de intervenciones oportunas. Con un enfoque en la accesibilidad y la simplicidad, los estudiantes han creado un recurso esencial que no solo empodera a las personas en el cuidado de su salud, sino que también fortalece la capacidad de respuesta de los servicios sanitarios locales, contribuyendo así a la construcción de comunidades más saludables y resilientes.</p>
